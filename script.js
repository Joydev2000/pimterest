function showcards(){
    var boxx = "";
    img.forEach(function(obj){
        boxx += `<div class="box">
        <img src="${obj.image}" alt="">
        <div class="subject">
            <p></p>
        </div>
    </div> `;
  document.querySelector(".content").innerHTML = boxx;
    });
}
showcards();

function searchhandle() {
    var search = document.querySelector(".scarch");
    
    search.addEventListener("focus", function () {
        document.querySelector(".overlay").style.display = "block";
      
        
    });

    search.addEventListener("blur", function () {
        document.querySelector(".overlay").style.display = "none";
        document.querySelector(".searchdata").style.display = "none";
        
    });

    search.addEventListener("input", function(){
        let filteredArray;
        if(search.value === ''){
            filteredArray = img;
        }
        else{
            
         filteredArray = img.filter(obj => obj.subject.toLowerCase().startsWith(search.value.toLowerCase()));
         var suggest = "";
         filteredArray.forEach(obj => {
             suggest += `<span> <i class="ri-search-line font-semibold mr-5"></i> <p>${obj.subject}</p></span>`
         });
         document.querySelector(".searchdata").style.display = "block";
         document.querySelector(".searchdata").innerHTML = suggest;

        }
    });
}

searchhandle();
